def fix_coords():
    conn = sqlite3.connect(str(DB_PATH))
    conn.row_factory = sqlite3.Row
    cur = conn.cursor()

    updated = 0
    for city, (lat, lon) in KNOWN_COORDS.items():
        # 数据库里城市带"市"后缀，做多种尝试
        for name_variant in [city, f"{city}市", f"{city}地区", f"{city}自治州"]:
            cur.execute(
                "UPDATE geo_cities SET lat=?, lon=? WHERE name=?",
                (lat, lon, name_variant),
            )
            if cur.rowcount > 0:
                updated += 1
                break

    conn.commit()

    print(f"Updated {updated} cities")

    # 统计
    total = cur.execute("SELECT COUNT(*) FROM geo_cities").fetchone()[0]
    no_coord = cur.execute(
        "SELECT COUNT(*) FROM geo_cities WHERE lat IS NULL OR lon IS NULL"
    ).fetchone()[0]
    print(f"Total cities: {total - no_coord}/{total} have coordinates")


if __name__ == "__main__":
    fix_coords()